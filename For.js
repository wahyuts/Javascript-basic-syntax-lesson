var i = 1
// kegunaan fungsi for adalah sama dengan fungsi while,..fungsi akan berjalan sampai dengan kondisi tertentu
// Perbedaannya dengan while adalah fungsi for penulsan syntaxnya lebih sederhana
function cobaRunFor () {
    for(i=1; i<5; i++){
        console.log(i);
    }
}

//maksud i = 1 itu (yang didalam tanda kurung for) adalah mendeklarasikan ulang variable i
// i <5 maksudnya adalah batas pengulangan fungsi for,..selama i < 5 fungsi akan dijalankan
// i++ maksudnya i akan bertambah otomais 1 di akhir fungsi dijalankan