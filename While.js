var i = 1
// fungsi while akan melakukan fungsi berulang sampaii dengan kondisi tertentu
// dalam hal ini fungsi akan dilakukan sampai dengan kondisi i =5 baru berhenti
// selama i kurang dari 5 maka akan berjalan terus otomatis
function cobaRunWhile () {
    while(i < 5){
        console.log(i);
        i++
    }
}